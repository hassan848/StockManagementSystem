    public static ArrayList<Classes.customersPage> listOfCustomers(){
        ArrayList<Classes.customersPage> listCustomers = new ArrayList<>();
        
        PreparedStatement st;
        ResultSet rs;
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement("SELECT * FROM `customers`");
            rs = st.executeQuery();
            
            Classes.customersPage currentCustomer;
            
            while(rs.next()){
                currentCustomer = new Classes.customersPage(rs.getInt("CustomerID"),rs.getString("firstName"),rs.getString("lastName"),rs.getString("ContactNumber"),rs.getString("Email"),rs.getString("postcode"));
                listCustomers.add(currentCustomer);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Classes.UsersPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listCustomers;
    }
    
    public static void displayCustomersListInJtable(){
        //Classes.UsersPage selectedUser = new Classes.UsersPage();
        ArrayList<Classes.customersPage> listCustomers = listOfCustomers();
        
        String[] JtableHeadings = {"Customer ID","First Name","Last Name","Contact Number", "Email", "Postcode"};
        Object[][] JtableRows = new Object[listCustomers.size()][6];
        
        for(int i=0;i<listCustomers.size();i++){
            JtableRows[i][0] = listCustomers.get(i).getID();
            JtableRows[i][1] = listCustomers.get(i).getFirstName();
            JtableRows[i][2] = listCustomers.get(i).getLastName();
            JtableRows[i][3] = listCustomers.get(i).getContactNumber();
            JtableRows[i][4] = listCustomers.get(i).getEmail();
            JtableRows[i][5] = listCustomers.get(i).getPostcode();
        }
        DefaultTableModel tableDisplayData = new DefaultTableModel(JtableRows,JtableHeadings);
        jTableCustomers.setModel(tableDisplayData);
        jTableCustomers.setRowHeight(45);
    }
    
    
