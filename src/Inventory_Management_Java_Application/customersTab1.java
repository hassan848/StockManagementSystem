    public void searchData(String query){
        PreparedStatement st;
        ResultSet rs;
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement(query);
            rs = st.executeQuery();
            DefaultTableModel tableModel = (DefaultTableModel)jTableCustomers.getModel();
            tableModel.setRowCount(0);
            
            if(rs.isBeforeFirst()){
                while(rs.next()){
                    Object userObject[] = {rs.getString("CustomerID"),rs.getString("firstName"),rs.getString("lastName"),rs.getString("contactNumber"),rs.getString("Email"),rs.getString("postcode")};
                    tableModel.addRow(userObject);
                }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(UsersTab.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
