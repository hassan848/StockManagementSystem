    public void setDigitalReceipt(){
        
        DigitalReceipt.setText("");
        
        DigitalReceipt.setText("   ------------------------Order Information----------------------\n\n");
        
        int orderID = (int) jTableAllOrders.getValueAt(jTableAllOrders.getSelectedRow(), 0);
        //System.out.println("Orders ID: "+orderID);
        ArrayList<Integer> productsIDList = Classes.makeOrder.getProductsIDList(orderID);
        ArrayList<Integer> quantityList = Classes.makeOrder.getProductQuantity(orderID);
        ArrayList<Double> productPrice = Classes.makeOrder.getProductPrice(orderID);
        
        ArrayList<String> productsName = new ArrayList<>();
        //System.out.println("Length "+productsIDList.size());
        for(int i=0; i<productsIDList.size();i++){
            productsName.add(Classes.makeOrder.getProductsNameList(productsIDList.get(i)));
            //System.out.println(quantityList.get(i)+" "+productPrice.get(i)+" "+productsIDList.get(i));
        }
        
        Font f1 = new Font("Courier New", Font.BOLD,12);
        DigitalReceipt.setFont(f1);
        DigitalReceipt.append("   Order ID: "+ orderID+"\n");
        
        Font f2 = new Font("Courier New", Font.PLAIN,12);
        DigitalReceipt.setFont(f2);
        
        //adjust product Names to fit onto word documents
        for(int a=0; a<productsName.size(); a++){
            while(productsName.get(a).length()<25){
                productsName.set(a, productsName.get(a) + " ");
            }
        }
        
        
        for(int x=0; x<productsIDList.size();x++){
            //System.out.println(productsName.get(x));
            DigitalReceipt.append("   "+ productsName.get(x)+"     "+quantityList.get(x)+"   @" + "   £"+productPrice.get(x)+"\n");
        }
        
        String totalPrice = jTableAllOrders.getValueAt(jTableAllOrders.getSelectedRow(), 4).toString().substring(1); 
        DigitalReceipt.append("\n   Total Price:   £"+totalPrice+"\n\n");
        
        DigitalReceipt.append("   ------------------------Customer Information-------------------\n\n");
        Classes.customersPage customerInfo = Classes.customersPage.getCusomerInfo(orderID);
        int customerID = customerInfo.getID();
    
        DigitalReceipt.append("   Customer ID: "+customerID);
        DigitalReceipt.append("\n   First Name: "+customerInfo.getFirstName());
        DigitalReceipt.append("\n   Last Name: "+customerInfo.getLastName());
        DigitalReceipt.append("\n   Contact Number: "+customerInfo.getContactNumber());
        DigitalReceipt.append("\n   Email: "+customerInfo.getEmail());
        DigitalReceipt.append("\n   Postcode: "+customerInfo.getPostcode());
        
    }
    
    
    
    public static void displayOrdersInJtable(){
        Classes.getOrderNReceipt orders = new Classes.getOrderNReceipt();
        ArrayList<Classes.getOrderNReceipt> listOfOrders = orders.allOrders();
        
        String[] JtableHeadings = {"Order ID", "Customer", "Date Created", "Time Created", "Total Price"};
        Object[][] JtableRows = new Object[listOfOrders.size()][5];
        
        for(int i=0; i<listOfOrders.size(); i++){
            JtableRows[i][0] = listOfOrders.get(i).getOrderID();
            JtableRows[i][1] = listOfOrders.get(i).getCustomerName();
            JtableRows[i][2] = listOfOrders.get(i).getDate();
            JtableRows[i][3] = listOfOrders.get(i).getTime();
            JtableRows[i][4] = "£"+listOfOrders.get(i).getTotalPrice();
        }
        
        DefaultTableModel tableDisplayData = new DefaultTableModel(JtableRows,JtableHeadings);
        jTableAllOrders.setModel(tableDisplayData);
        jTableAllOrders.setRowHeight(45);
        
        
    }
    
    
    
