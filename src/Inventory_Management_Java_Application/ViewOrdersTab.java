    private void openReceiptBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openReceiptBTNActionPerformed
        if(!jTableAllOrders.getSelectionModel().isSelectionEmpty()){
            
            int orderID = (int) jTableAllOrders.getValueAt(jTableAllOrders.getSelectedRow(), 0);
            String date = jTableAllOrders.getValueAt(jTableAllOrders.getSelectedRow(), 2).toString();
            
            Classes.generateWordReceipt.openReceiptFile(date, orderID);
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Please Select An Order", "There Is No Order Selected!", 0);
        }
    }//GEN-LAST:event_openReceiptBTNActionPerformed

    private void deleteOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrderBtnActionPerformed
        if(!jTableAllOrders.getSelectionModel().isSelectionEmpty()){
            
            int orderID = (int) jTableAllOrders.getValueAt(jTableAllOrders.getSelectedRow(), 0);
            Classes.getOrderNReceipt.deleteOrder(orderID);
            
            JOptionPane.showMessageDialog(null, "Order Has Been Deleted");
            clearBtnActionPerformed(null);
            displayOrdersInJtable();
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Please Select An Order", "There Is No Order Selected!", 0);
        }
    }//GEN-LAST:event_deleteOrderBtnActionPerformed

