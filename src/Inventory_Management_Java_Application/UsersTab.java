    public boolean checkIfFieldsEmpty(){
        if(jTextFieldUsername.getText().equals("") || jTextFieldName.getText().equals("") || jTextFieldNumber.getText().equals("") || jTextFieldEmail.getText().equals("")){
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty", "Empty Fields", 0);
            return false;
        }else{
            return true;
        }
    }
    
    public boolean checkUsername(){
        PreparedStatement st;
        ResultSet rs;
        String query = "SELECT `Username` FROM `userslog` WHERE `Username` = ?";
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement(query);
            st.setString(1, jTextFieldUsername.getText());
            rs = st.executeQuery();
            if(rs.next()){
                return false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(UsersTab.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }
    
