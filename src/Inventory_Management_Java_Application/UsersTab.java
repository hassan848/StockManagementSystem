    public void searchData(String query){
        PreparedStatement st;
        ResultSet rs;
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement(query);
            rs = st.executeQuery();
            DefaultTableModel tableModel = (DefaultTableModel)jTable1.getModel();
            tableModel.setRowCount(0);
            
            if(rs.isBeforeFirst()){
                while(rs.next()){
                    Object userObject[] = {rs.getString("Username"),rs.getString("FullName"),rs.getString("ContactNumber"),rs.getString("Email"),rs.getString("UserType")};
                    tableModel.addRow(userObject);
                }
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(UsersTab.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
