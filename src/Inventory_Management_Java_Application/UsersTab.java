    public static ArrayList<Classes.UsersPage> listOfUsers(){
        ArrayList<Classes.UsersPage> listUsers = new ArrayList<>();
        
        PreparedStatement st;
        ResultSet rs;
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement("SELECT * FROM `userslog`");
            rs = st.executeQuery();
            
            Classes.UsersPage currentUser;
            
            while(rs.next()){
                currentUser = new Classes.UsersPage(rs.getString("Username"),rs.getString("UserPassword"),rs.getString("FullName"),rs.getString("ContactNumber"),rs.getString("Email"),rs.getString("UserType"));
                listUsers.add(currentUser);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Classes.UsersPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listUsers;
    }
    
