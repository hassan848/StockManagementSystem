    public static ArrayList<Classes.productsPage> listOfProducts(){
        ArrayList<Classes.productsPage> listProducts = new ArrayList<>();
        
        PreparedStatement st;
        ResultSet rs;
        
        try {
            st = MyConnectionDB.getConnection().prepareStatement("SELECT * FROM `products`");
            rs = st.executeQuery();
            
            Classes.productsPage currentProducts;
            
            while(rs.next()){
                currentProducts = new Classes.productsPage(rs.getInt("productID"), rs.getString("productName"), rs.getInt("remainingStock"), rs.getDouble("productPrice"), rs.getString("productDescription"), rs.getInt("productTypeID"), rs.getBytes("productImage"));
                listProducts.add(currentProducts);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(productsTab.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listProducts;
    }
    
   
    
