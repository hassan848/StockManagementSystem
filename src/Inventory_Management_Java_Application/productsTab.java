    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // Checking if a product has been selected from the Jtable
        if(!jTableProducts.getSelectionModel().isSelectionEmpty()){
            //getting row for selected product
            int i = jTableProducts.getSelectedRow();
         
            
            //creating editProductForm object to set values of selected product into edit form
            editProductJForm editProduct = new editProductJForm();
           
            if(jTableProducts.getValueAt(i, 6)==null){
                editProduct.jComboBoxPtype.setSelectedItem("");
            }else{
                editProduct.jComboBoxPtype.setSelectedItem(jTableProducts.getValueAt(i, 6).toString());
            }
           
            editProduct.productID = Integer.parseInt(jTableProducts.getValueAt(i, 0).toString());
            editProduct.jTextFieldName.setText(jTableProducts.getValueAt(i, 1).toString());
            editProduct.jTextFieldPrice.setText(jTableProducts.getValueAt(i, 3).toString().substring(1));
            editProduct.jTextFieldStock.setText(jTableProducts.getValueAt(i, 2).toString());
            editProduct.jTextAreaDescription.setText(jTableProducts.getValueAt(i, 4).toString());
            //set image for edit form
            ImageIcon tableImage = (ImageIcon)jTableProducts.getValueAt(i, 5);
            Image img = tableImage.getImage().getScaledInstance(editProduct.jLabelImage.getWidth(), editProduct.jLabelImage.getHeight(),Image.SCALE_SMOOTH);
            tableImage = new ImageIcon(img);
            editProduct.jLabelImage.setIcon(tableImage);
     
            
            //call edit form page
            editProduct.setVisible(true);
            editProduct.pack();
            editProduct.setLocationRelativeTo(null);
            editProduct.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            
            
        }else{
            JOptionPane.showMessageDialog(null, "Please Select A Product", "There Is No Product Selected!", 0);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if(!jTableProducts.getSelectionModel().isSelectionEmpty()){
            int decision = JOptionPane.showConfirmDialog(null,"Are you sure you want to delete this product?","Delete Product", JOptionPane.YES_NO_OPTION);
            if(decision == 0){
                int i = jTableProducts.getSelectedRow();
                Integer currentProductID = Integer.valueOf(jTableProducts.getValueAt(i, 0).toString());
                Classes.productsPage.deleteProduct(currentProductID);
                JOptionPane.showMessageDialog(null, "Product Has Been Deleted");

                
                jButtonClearActionPerformed(null);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Select A Product", "There Is No Product Selected!", 0);
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

